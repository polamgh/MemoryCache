import Foundation
typealias PastebinModel = [PurplePastebinModel]

struct PurplePastebinModel: Codable {
    let id, createdAt: String
    let width, height: Int
    let color: String
    let likes: Int
    let likedByUser: Bool
    let user: User
    let currentUserCollections: [JSONAny]
    let urls: Urls
    let categories: [Category]
    let links: PastebinModelLinks

    enum CodingKeys: String, CodingKey {
        case id
        case createdAt = "created_at"
        case width, height, color, likes
        case likedByUser = "liked_by_user"
        case user
        case currentUserCollections = "current_user_collections"
        case urls, categories, links
    }
}

struct Category: Codable {
    let id: Int
    let title: Title
    let photoCount: Int
    let links: CategoryLinks

    enum CodingKeys: String, CodingKey {
        case id, title
        case photoCount = "photo_count"
        case links
    }
}

struct CategoryLinks: Codable {
    let purpleSelf: PurpleSelf
    let photos: Photos

    enum CodingKeys: String, CodingKey {
        case purpleSelf = "self"
        case photos
    }
}

enum Photos: String, Codable {
